print(type(1))
# <class 'int'>

print(type(True))
# <class 'bool'>

print(type('Hello'))
# <class 'str'>

print(type([1, 2, 3]))
# <class 'list'>

print(type((1, 2, 3)))
# <class 'tuple'>

print(type({'a': 1, 'b': 2}))
# <class 'dict'>

print(type({1, 2, 3}))
# <class 'set'>

print(type(None))
# <class 'NoneType'>


var = 1
print(type(var))
# <class 'int'>


